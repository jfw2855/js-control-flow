
// Logs an age category based on age


let age = 0;

if (age<1) {
    console.log("infant");
}
else if (age>=1&&age<3) {
    console.log("toddler");
}
else if (age>=3&&age<9) {
    console.log("child");
}
else if (age>=9&&age<13) {
    console.log("preteen");
}
else if (age>=13&&age<18) {
    console.log("teen");
}
else if (age>=18&&age<21) {
    console.log("young adult");
}
else {
    console.log("adult");
}
